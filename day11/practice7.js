/* 1. 목표
- 사용자가 자신의 수입/지출 내역을 날짜, 항목, 금액으로 기록하고, 
등록된 모든 내역을 한눈에 볼 수 있는 간단한 가계부 웹 페이지를 만듭니다.

2. 화면 구성
- 페이지는 사용자가 정보를 입력하는 '등록 영역'과 그 결과를 보여주는
'목록 영역'으로 명확히 구분되어야 합니다.

- 등록 영역:
- 날짜 선택: 사용자가 내역의 날짜를 달력에서 직접 선택할 수 있어야 합니다.
- 항목 입력: 사용자가 지출 또는 수입 항목의 내용을 텍스트로 자유롭게 입력할 수 있어야 합니다.
            (예: "점심 식사", "교통비")
- 금액 입력: 사용자가 금액을 숫자로 입력할 수 있어야 합니다.
- 등록 버튼: 위 세 가지 정보를 입력한 후, 목록에 추가하기 위한 "등록" 버튼이 있어야 합니다.
- 목록 영역:
            등록된 모든 가계부 내역을 보여주는 표가 있어야 합니다.
            표는 "날짜", "항목", "금액" 순서의 열로 구성되어야 합니다.

3. 핵심 기능

- 초기 데이터 표시:
        사용자가 페이지에 처음 방문했을 때, 가계부 사용법을 쉽게 이해할 수 있도록 
        두 개의 예시 내역이 목록 표에 기본적으로 표시되어 있어야 합니다.

- 내역 등록 기능:
        사용자가 '등록 영역'에 날짜, 항목, 금액을 모두 입력하고 "등록" 버튼을 클릭하면, 
        해당 내역이 '목록 영역' 표의 가장 아래쪽에 새로운 행으로 즉시 추가되어야 합니다.

- 금액 자동 서식:
        '목록 영역'에 표시되는 모든 금액은 사용자의 가독성을 위해 
        천 단위마다 쉼표(,)가 자동으로 표시되어야 합니다. 
        (예: 4500이 입력되면 "4,500"으로 보여야 함)
        힌트] 변수명.toLocaleString() :숫자를 세 자리마다 콤마(,)로 구분해주는 유용한 함수 */

// 1. 프로토타입 : 피그마로 설계
// 2. 메모리설계 : 날짜, 항목, 금액, 배열 = 표 / 객체 = 행 / 속성 = 열   
let 가계부목록 = [ 
    { 날짜 : '2025-06-19' , 항목 : "점심 식사" , 금액 : 9000},
    { 날짜 : '2025-06-19' , 항목 : "교통비" , 금액 : 1500 } ]
// 3. 기능설계 : 등록, 출력 
    // 1.등록함수 : 등록버튼 클릭하면 , 3개의 입력받은 값들을 가져와서 가계부목록(배열)에 객체로 저장
    // 2.출력함수 : 페이지가열리면/등록성공하면 , 현재 가계부목록(배열)에 저장된 객체들을 테이블에 출력
function 등록함수( ){
    // 1. 특정한 마크업DOM 에 입력받은 값 가져오기 , 서로 다른 3곳에 , HT(텍스트)ML
    let 날짜 = document.querySelector('.날짜입력').value;
    let 항목 = document.querySelector('.항목입력').value;
    let 금액 = document.querySelector('.금액입력').value; // 입력받은 값들을 문자열로 반환 
    // 2. 객체 구성 
    let 객체 = { 날짜 , 항목 , 금액 }// vs 객체생성시 속성명과 속성값의변수명이 같으면 생략 가능 
    // vs let 객체 = { 날짜 : 날짜 , 항목 : 항목 , 금액 : 금액 }
    가계부목록.push( 객체 ) // 3. 배열 저장
    alert('등록성공') // 4. 성공 안내
    출력함수( ) // ****** [2] 등록 성공시 출력함수 실행 ******* 
} // f end 

function 출력함수( ){
    // 1. 어디에
    let 테이블 = document.querySelector('table > tbody')
    // 2. 무엇을 , 배열내 모든 자료(객체)들을 HTML(문자) 형식으로 
    let html = ""
    for( let index = 0 ; index <= 가계부목록.length-1 ; index++ ){
        let 가계부 = 가계부목록[index]; // index번째 가계부객체1개
        // 주의할점 : html = 대입(기존자료사라짐) 
                // vs html += 누적(기존자료뒤에연결)
        html += `<tr>
                    <td> ${ 가계부.날짜 } </td>
                    <td> ${ 가계부.항목 } </td>
                    <td> ${ 가계부.금액.toLocaleString() } 원 </td>
                </tr>`
    } // for end 
    // 3. 출력 
    테이블.innerHTML = html;
} // f end 
